/*  Escribe un programa de tres líneas que pida un número, pida otro número y escriba el resultado de sumar estos dos números.
Nota: Tener en cuenta la siguiente función: parseInt */

const numero1 = parseInt(prompt("Ingrese el primer numero a sumar: "))
const numero2 = parseInt(prompt("Ingrese el segundo numero a sumar: "))
alert("el resultado de la suma es: " + (numero1+numero2)) 